import { getLastestPosts } from '@firebase/client'
import { HeaderMobile } from 'components/Utils/HeaderMobile'
import { NavBarMobile } from 'components/Utils/NavBarMobile'
import { PostCard } from 'components/Posts/PostCard'
import { useAuth } from 'context/authUserContext'
import Head from 'next/head'
import { useRouter } from 'next/router'
import { useEffect, useState } from 'react'
import { PostCollection } from 'types/databaseTypes'
import { SideBarContainer } from 'components/SideBars/SideBarContainer'

export default function Home () {
  const [search, setSearch] = useState('')
  const [posts, setPosts] = useState<PostCollection[]>([])
  const auth = useAuth()
  const router = useRouter()

  useEffect(() => {
    if (!auth.authUser) router.push('/login')

    if (auth.authUser) {
      const unsub = getLastestPosts(setPosts)
      return () => unsub && unsub()
    }
  }, [auth.authUser])

  const fullName = auth.authUser?.displayName?.split('|')[0]

  return (
    <>
      <Head>
        <title>{fullName} | Connet</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="logo_ico.ico" />
      </Head>
      <SideBarContainer />
      <main className='relative w-full h-screen'>
        <HeaderMobile search={search} setSearch={setSearch}/>

        <div className='flex flex-col gap-4 h-screen overflow-scroll no-scrollbar'>
          {
            posts && posts.map(post => {
              if (post.content?.toLowerCase().includes(search)) {
                return <PostCard
                  post={post}
                  key={post.id}
                />
              }
              return undefined
            })
          }

        </div>

      </main>

      <NavBarMobile />
    </>
  )
}
